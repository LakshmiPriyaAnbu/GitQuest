function assert(condition, message) {
  if (!condition) {
    throw new Error(message);
  }
}

function assertRequiredTopLevel(data, schema, label) {
  for (const key of schema.required ?? []) {
    assert(key in data, `${label}: missing required top-level key "${key}"`);
  }
}

function validateContent(content) {
  const routeIds = new Set();
  const routePaths = new Set();
  const groupRouteIds = new Set();

  for (const route of content.routes) {
    assert(typeof route.id === 'string' && route.id.length > 0, 'content.routes: each route needs a non-empty id');
    assert(typeof route.path === 'string' && route.path.startsWith('/'), `content.routes.${route.id}: path must start with /`);
    assert(!routeIds.has(route.id), `content.routes: duplicate route id "${route.id}"`);
    assert(!routePaths.has(route.path), `content.routes: duplicate route path "${route.path}"`);
    routeIds.add(route.id);
    routePaths.add(route.path);
  }

  for (const group of content.navGroups) {
    assert(typeof group.title === 'string' && group.title.length > 0, 'content.navGroups: each group needs a title');
    for (const routeId of group.routeIds) {
      assert(routeIds.has(routeId), `content.navGroups.${group.title}: unknown route id "${routeId}"`);
      groupRouteIds.add(routeId);
    }
  }

  for (const routeId of content.mobileNavRouteIds) {
    assert(routeIds.has(routeId), `content.mobileNavRouteIds: unknown route id "${routeId}"`);
  }

  for (const routeId of content.moreRouteIds) {
    assert(routeIds.has(routeId), `content.moreRouteIds: unknown route id "${routeId}"`);
  }

  for (const feature of content.dashboard.features) {
    assert(routeIds.has(feature.routeId), `content.dashboard.features: unknown route id "${feature.routeId}"`);
  }

  for (const quest of content.quests.items) {
    assert(routePaths.has(quest.route), `content.quests.items.${quest.id}: unknown route path "${quest.route}"`);
  }

  assert(content.commandLab.lessons.length > 0, 'content.commandLab.lessons must not be empty');
  assert(content.learnCards.length > 0, 'content.learnCards must not be empty');
  assert(content.internalsCards.length > 0, 'content.internalsCards must not be empty');
  assert(content.badges.length > 0, 'content.badges must not be empty');
  assert(content.quests.items.length > 0, 'content.quests.items must not be empty');
  assert(content.progression.levelTiers.length === content.progression.levelTitles.length, 'content.progression: tiers and titles must be the same length');

  return true;
}

function validateTheme(theme) {
  assert(theme.themes.nebula, 'theme.themes.nebula is required');
  assert(theme.themes.candy, 'theme.themes.candy is required');
  assert(Array.isArray(theme.laneColors) && theme.laneColors.length > 0, 'theme.laneColors must not be empty');
  assert(Array.isArray(theme.commitGradients) && theme.commitGradients.length > 0, 'theme.commitGradients must not be empty');

  for (const [name, palette] of Object.entries(theme.palettes)) {
    assert(palette.bg && palette.value && palette.label, `theme.palettes.${name} must define bg, value, and label`);
  }

  for (const [name, style] of Object.entries(theme.toastStyles)) {
    assert(style.bg && style.icon && style.fg, `theme.toastStyles.${name} must define bg, icon, and fg`);
  }

  return true;
}

